function separateByStatus(data) {
  const courses = data.courses || [];
  return courses.reduce((acc, course) => {
    const status = course.status;
    if (!acc[status]) {
      acc[status] = [];
    }
    acc[status].push(course);
    return acc;
  }, {});
}

function sortCoursesByStatus(data) {
  const courses = data.courses || [];

  // Custom sorting: "ongoing" comes first, "completed" comes last
  courses.sort((a, b) => {
    const statusOrder = { "ongoing": 1, "completed": 2 };
    return (statusOrder[a.status] || 3) - (statusOrder[b.status] || 3);
  });

  return courses;
}




	
