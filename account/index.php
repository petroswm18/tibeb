function daysAgo(dateString) {
    // Convert the date string to a Date object
    const pastDate = new Date(dateString);
    
    // Get the current date
    const currentDate = new Date();
    
    // Calculate the difference in time (in milliseconds)
    const timeDiff = currentDate - pastDate;
    
    // Convert the time difference from milliseconds to days
    var daysAgo = Math.floor(timeDiff / (1000 * 60 * 60 * 24));
	
	if(daysAgo==0){
		daysAgo="Today";
	}
	else if(daysAgo==1){
		daysAgo="Yesterday";
	}
	else{
		daysAgo=daysAgo+" days ago";
	}
    
    return daysAgo;
}



function countStatus(data, targetStatus) {
  const courses = data.courses || [];
  return courses.filter(course => course.status === targetStatus).length;
}

